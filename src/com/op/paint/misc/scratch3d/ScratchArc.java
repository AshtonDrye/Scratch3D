package com.op.paint.misc.scratch3d;

public class ScratchArc {
	double xtl = 0;
	double ytl = 0;
	double d = 0;
	int angStart = 0;
	int angArcDraw = 0;

	public ScratchArc(double xtl, double ytl, double d, int angStart,
			int angArcDraw) {
		this.xtl = xtl;
		this.ytl = ytl;
		this.d = d;
		this.angStart = angStart;
		this.angArcDraw = angArcDraw;
	}
}
